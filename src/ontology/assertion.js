"use strict";

export default class Assertion {
  constructor(string, node, tokens) {
    this.string = string;
    this.node = node;
    this.tokens = tokens;
  }

  getString() {
    return this.string;
  }

  getNode() {
    return this.node;
  }

  getTokens() {
    return this.tokens;
  }

  matchAssertionNode(assertionNode) { return this.node.match(assertionNode); }
}
